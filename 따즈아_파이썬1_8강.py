''' 4) 튜플(tuple) : 리스트와 비슷한 자료형이다.
    - 리스트와 튜플의 차이점
     . 리스트는 [], 튜플은 ()를 사용한다.
     . 리스트는 요소의 변경(수정, 삭제, 생성)이 가능하지만, 튜플은 요소의 값을 변경할 수 없다.


    사용예>
    tu = () --> 빈값이 들어있는 형태
    tu2 = (1,)
    tu3 = (10, 20, 30, 40)
    tu4 = 10, 20, 30
    tu5 = ("국제시장", "명량",("ab", "cd"))

.튜플의 인덱싱, 슬라이싱, 연산
'''

tu = ('a','b','c',10, 1000)

print(tu[0])

print(tu[2:])

tu2 = ('d', 'e', 'f')
print(tu+tu2)

print(tu*3)


